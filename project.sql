CREATE TABLE TEMPERATURES(
    ID_TEMP INT AUTO_INCREMENT PRIMARY KEY,
    DATE_TEMP DATE,
    DEP_ID VARCHAR(3),
    DEPARTMENT VARCHAR(50),
    MIN_TEMP DECIMAL,
    MAX_TEMP DECIMAL,
    MEAN_TEMP DECIMAL
);

ALTER TABLE TEMPERATURES ADD COLUMN ID_TEMP INT AUTO INCREMENT PRIMARY KEY;

CREATE TABLE HOSPITALISATIONS(
    ID_HOP INT AUTO_INCREMENT PRIMARY KEY,
    SEX INT,
    DATE_HOSP DATE,
    NUM_HOSP INT,
    NUM_INTENSE_CARE INT,
    NUM_CONV_HOSP INT,
    NUM_SSR_USLD INT,
    NUM_OTHERS INT,
    NUM_RETURN_HOME INT,
    NUM_DEATHS INT
)

CREATE VIEW DATES_TEMP_VIEW AS
SELECT ID_TEMP, DATE_TEMP, DEP_ID
FROM TEMPERATURES;

CREATE VIEW DEPS_TEMP_VIEW AS
SELECT ID_TEMP, DEP_ID, MEAN_TEMP
FROM TEMPERATURES;


